package core_java_concepts;

public class Logical_comparision_IfElseIf {

	public static void main(String[] args) {
		
		int a=40;
		int b=50;
		if(a<b)
		{
			System.out.println(a+" is small");
		}
		else if(a==b)
		{
			System.out.println(a+" and"+b+" are equal");
		}
		else
		{
			System.out.println(a+" is big");
		}
		
	}

}
